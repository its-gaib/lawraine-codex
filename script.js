const icons = ["§", "∆", "↔", "◎", "=", "◇", "¶", "→"];
const supportedLanguages = ["de", "fr", "en", "it"];
const lawyerPhotos = {
  "thomaswenger@lawraine.ch": "assets/thomas-wenger.png",
  "sonjacomte@lawraine.ch": "assets/sonja-comte.jpg"
};

function resolveLanguage() {
  const params = new URLSearchParams(window.location.search);
  const requested = params.get("lang");
  if (supportedLanguages.includes(requested)) return requested;
  return "de";
}

function getValue(source, path) {
  return path.split(".").reduce((value, key) => value?.[key], source);
}

function renderPracticeCards(language) {
  const grid = document.querySelector("#practice-grid");
  grid.innerHTML = translations[language].practices
    .map(([title, text], index) => `
      <article class="practice-card">
        <span class="icon" aria-hidden="true">${icons[index]}</span>
        <h3>${title}</h3>
        <p>${text}</p>
      </article>
    `)
    .join("");
}

function renderLawyers(language) {
  const grid = document.querySelector("#team-grid");
  grid.innerHTML = translations[language].lawyers
    .map((lawyer) => `
      <article class="lawyer-card">
        <img class="portrait" src="${lawyerPhotos[lawyer.email]}" alt="${lawyer.name}" width="176" height="176" loading="lazy">
        <div>
          <h3>${lawyer.name}</h3>
          <p><strong>${lawyer.role}</strong></p>
          <p>${lawyer.summary}</p>
          <p>${lawyer.extra}</p>
          <a href="mailto:${lawyer.email}">${lawyer.email}</a>
          <ul>${lawyer.tags.map((tag) => `<li>${tag}</li>`).join("")}</ul>
        </div>
      </article>
    `)
    .join("");
}

function applyLanguage(language, updateUrl = true) {
  const dictionary = translations[language];
  document.documentElement.lang = language;
  document.title = {
    de: "Advokatur LawRaine | Anwaltskanzlei in Bern",
    fr: "Advokatur LawRaine | Étude d'avocats à Berne",
    en: "Advokatur LawRaine | Law firm in Bern",
    it: "Advokatur LawRaine | Studio legale a Berna"
  }[language];

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = getValue(dictionary, node.dataset.i18n);
  });

  document.querySelectorAll("[data-lang]").forEach((button) => {
    button.setAttribute("aria-pressed", String(button.dataset.lang === language));
  });

  renderPracticeCards(language);
  renderLawyers(language);

  if (updateUrl) {
    const url = new URL(window.location.href);
    url.searchParams.set("lang", language);
    window.history.replaceState({}, "", url);
  }
}

document.querySelectorAll("[data-lang]").forEach((button) => {
  button.addEventListener("click", () => applyLanguage(button.dataset.lang));
});

document.querySelector("#year").textContent = new Date().getFullYear();
applyLanguage(resolveLanguage(), false);
